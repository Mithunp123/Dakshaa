    FOR ALL USING (get_user_role() = 'super_admin');

CREATE POLICY "Super Admins can do everything on combos" ON combos
    FOR ALL USING (get_user_role() = 'super_admin');

CREATE POLICY "Super Admins can do everything on combo_rules" ON combo_rules
    FOR ALL USING (get_user_role() = 'super_admin');

CREATE POLICY "Staff can view all registrations" ON registrations
    FOR SELECT USING (
        get_user_role() IN ('super_admin', 'registration_admin') OR 
        is_event_coordinator(event_id)
    );

-- Teams: Users can view teams they are members of
CREATE POLICY "Users can view teams they are members of" ON teams
    FOR SELECT USING (
        EXISTS (
            SELECT 1 FROM team_members 
            WHERE team_members.team_id = teams.id 
            AND team_members.user_id = auth.uid()
        )
    );

-- Team Members: Users can view team members of their teams
-- Using a security definer function to avoid infinite recursion in RLS
CREATE OR REPLACE FUNCTION check_team_membership(t_id UUID)
RETURNS BOOLEAN AS $$
BEGIN
    RETURN EXISTS (
        SELECT 1 FROM team_members
        WHERE team_id = t_id
        AND user_id = auth.uid()
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE POLICY "Users can view team members of their teams" ON team_members
    FOR SELECT USING (
        check_team_membership(team_id)
    );

-- 9. Attendance Table
CREATE TABLE IF NOT EXISTS attendance (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES profiles(id) ON DELETE CASCADE,
    event_id TEXT REFERENCES events(event_id),
    marked_by UUID REFERENCES profiles(id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    UNIQUE(user_id, event_id)
);

-- 10. Winners Table
CREATE TABLE IF NOT EXISTS winners (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    event_id TEXT REFERENCES events(event_id),
    user_id UUID REFERENCES profiles(id) ON DELETE CASCADE,
    position INTEGER NOT NULL, -- 1, 2, 3
    certificate_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    UNIQUE(event_id, position)
);

-- 11. Event Coordinators Table
CREATE TABLE IF NOT EXISTS event_coordinators (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES profiles(id) ON DELETE CASCADE,
    event_id TEXT REFERENCES events(event_id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    UNIQUE(user_id, event_id)
);

