-- Events: Everyone can read active events
CREATE POLICY "Allow public read access to active events" ON events
    FOR SELECT USING (is_active = true);

-- Profiles: Users can manage their own profile
CREATE POLICY "Users can view their own profile" ON profiles
    FOR SELECT USING (auth.uid() = id);

CREATE POLICY "Users can update their own profile" ON profiles
    FOR UPDATE USING (auth.uid() = id);

CREATE POLICY "Users can insert their own profile" ON profiles
    FOR INSERT WITH CHECK (auth.uid() = id);

-- Combos: Everyone can read active combos
CREATE POLICY "Allow public read access to active combos" ON combos
    FOR SELECT USING (is_active = true);

-- Combo Rules: Everyone can read combo rules
CREATE POLICY "Allow public read access to combo rules" ON combo_rules
    FOR SELECT USING (true);

-- Registrations: Users can read their own registrations
CREATE POLICY "Users can view their own registrations" ON registrations
    FOR SELECT USING (auth.uid() = user_id);

-- Registrations: Users can insert their own registrations
CREATE POLICY "Users can insert their own registrations" ON registrations
    FOR INSERT WITH CHECK (auth.uid() = user_id);

-- Admin Policies (Assuming a 'role' claim in auth.users or a separate profiles table)
