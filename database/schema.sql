-- Helper function to get user role
CREATE OR REPLACE FUNCTION get_user_role()
RETURNS TEXT AS $$
BEGIN
  RETURN (SELECT role FROM profiles WHERE id = auth.uid());
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Helper function to check if user is coordinator for an event
CREATE OR REPLACE FUNCTION is_event_coordinator(e_id TEXT)
RETURNS BOOLEAN AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM event_coordinators
    WHERE user_id = auth.uid() AND event_id = e_id
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

