-- 1. Events Table
CREATE TABLE IF NOT EXISTS events (
    event_id TEXT PRIMARY KEY,
    category TEXT NOT NULL,
    price DECIMAL NOT NULL DEFAULT 0,
    capacity INTEGER NOT NULL DEFAULT 100,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 2. Combos Table
CREATE TABLE IF NOT EXISTS combos (
    combo_id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    price DECIMAL NOT NULL DEFAULT 0,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 3. Combo Rules Table
CREATE TABLE IF NOT EXISTS combo_rules (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    combo_id TEXT REFERENCES combos(combo_id) ON DELETE CASCADE,
    category TEXT NOT NULL,
    allowed_count INTEGER NOT NULL DEFAULT 1,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 4. Registrations Table
CREATE TABLE IF NOT EXISTS registrations (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES profiles(id) ON DELETE CASCADE,
    event_id TEXT REFERENCES events(event_id),
    combo_id TEXT REFERENCES combos(combo_id),
    payment_status TEXT DEFAULT 'pending',
    payment_id TEXT,
    qr_code_string TEXT UNIQUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 5. Profiles Table
CREATE TABLE IF NOT EXISTS profiles (
    id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
    full_name TEXT NOT NULL,
    gender TEXT,
    college_name TEXT,
    department TEXT,
    year_of_study TEXT,
    roll_number TEXT,
    mobile_number TEXT,
    role TEXT DEFAULT 'student',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 6. Teams Table
CREATE TABLE IF NOT EXISTS teams (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    event_id TEXT REFERENCES events(event_id),
    created_by UUID REFERENCES profiles(id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 7. Team Members Table
CREATE TABLE IF NOT EXISTS team_members (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    team_id UUID REFERENCES teams(id) ON DELETE CASCADE,
    user_id UUID REFERENCES profiles(id),
    role TEXT DEFAULT 'member', -- 'lead' or 'member'
    status TEXT DEFAULT 'joined', -- 'invited' or 'joined'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    UNIQUE(team_id, user_id)
);

-- 8. Capacity Check Trigger
CREATE OR REPLACE FUNCTION check_capacity()
RETURNS TRIGGER AS $$
DECLARE
    current_count INTEGER;
    max_capacity INTEGER;
BEGIN
    -- Get current registration count for the event
    SELECT COUNT(*) INTO current_count FROM registrations WHERE event_id = NEW.event_id;
    
    -- Get capacity for the event
    SELECT capacity INTO max_capacity FROM events WHERE event_id = NEW.event_id;
    
    IF current_count >= max_capacity THEN
        RAISE EXCEPTION 'Event capacity reached';
    END IF;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_check_capacity ON registrations;
CREATE TRIGGER trg_check_capacity
BEFORE INSERT ON registrations
FOR EACH ROW
EXECUTE FUNCTION check_capacity();

