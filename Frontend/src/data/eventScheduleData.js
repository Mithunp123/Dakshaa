// Helper function to get day schedule
export const getDaySchedule = (day) => {
  return eventScheduleData[day] || null;
};

// Helper function to get event by ID
export const getEventById = (day, eventId) => {
  const dayData = eventScheduleData[day];
  if (!dayData) return null;
  return dayData.events.find(event => event.id === eventId);
};

// Helper function to get all events for a day sorted by time
export const getEventsSortedByTime = (day) => {
  const dayData = eventScheduleData[day];
  if (!dayData) return [];
  return [...dayData.events].sort((a, b) => {
    const timeA = new Date(`2026-02-01 ${a.startTime}`).getTime();
    const timeB = new Date(`2026-02-01 ${b.startTime}`).getTime();
    return timeA - timeB;
  });
};

// Helper function to get events by type for a day
export const getEventsByType = (day, type) => {
  const dayData = eventScheduleData[day];
  if (!dayData) return [];
  return dayData.events.filter(event => event.type === type);
};

// Populate coordinator data from eventCoordinatorsData
export const populateCoordinatorData = () => {
  [1, 2, 3].forEach(day => {
    const dayData = eventScheduleData[day];
    if (dayData && dayData.events) {
      dayData.events.forEach(event => {
        const coordinator = eventCoordinatorsData.find(coord => 
          coord.eventName.toLowerCase().includes(event.eventName.toLowerCase()) ||
          event.eventName.toLowerCase().includes(coord.eventName.toLowerCase())
        );
        
        if (coordinator) {
          if (!event.coordinatorName || event.coordinatorName === '') {
            event.coordinatorName = coordinator.coordinatorName;
          }
          if (!event.coordinatorNumber || event.coordinatorNumber === '') {
            event.coordinatorNumber = coordinator.coordinatorContact;
          }
        }
      });
    }
  });
};

// Get coordinator details for an event by name
export const getCoordinatorByEventName = (eventName) => {
  return eventCoordinatorsData.find(coord => 
    coord.eventName.toLowerCase() === eventName.toLowerCase()
  );
};
